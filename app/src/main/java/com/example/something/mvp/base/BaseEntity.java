package com.example.something.mvp.base;

import org.litepal.crud.DataSupport;

public class BaseEntity extends DataSupport {
    private String msg;
    private int code;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }
}
